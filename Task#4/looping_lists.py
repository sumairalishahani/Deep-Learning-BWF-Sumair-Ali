# 4-2. Animals:
animals=['dog','cat','lion','tiger','elephant'] 
for animal in animals:
    if animal == "dog":
        print("A dog would make a great pet.")
    elif animal == "cat":
        print("A cat would be a nice companion.")
    elif animal == "hamster":
        print("A hamster would be a fun pet to play with.")
    elif animal == "bird":
        print("A bird would be a great choice for a pet if you love to hear birdsong.")
    elif animal == "fish":
        print("A fish would be a good option if you want a low-maintenance pet.")
        
print("Any of these animals would make a great pet!")